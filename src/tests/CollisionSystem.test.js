import { describe, it, expect, beforeEach } from 'vitest';
import { CollisionSystem } from '../features/collision/system/CollisionSystem.ts';
import { MathUtils } from '../shared/utils/MathUtils.ts';

/**
 * ГРУППА ТЕСТОВ для системы коллизий
 *
 * describe() = "описываю тесты для..."
 * Это контейнер, который группирует связанные тесты вместе
 */
describe('Система коллизий - AABB проверка', () => {
    // Переменная для системы коллизий (доступна во всех тестах)
    let collisionSystem;

    /**
     * beforeEach() = "перед каждым тестом делай это"
     *
     * Зачем: Каждый тест должен начинаться с чистого состояния.
     * Это как сброс игры перед новым раундом.
     */
    beforeEach(() => {
        collisionSystem = new CollisionSystem();
    });

    /**
     * ТЕСТ 1: Проверка что столкновение ОБНАРУЖИВАЕТСЯ
     *
     * it() = "это должно..."
     * Внутри: проверяем одно конкретное поведение
     */
    it('должен обнаружить столкновение когда игрок врезается в препятствие', () => {
        // 1. ARRANGE (Подготовка) - создаём тестовые данные
        // Игрок на позиции (100, 200) размером 80x80
        const player = { x: 100, y: 200, width: 80, height: 80 };

        // Препятствие на позиции (120, 220) размером 100x100
        // Оно ПЕРЕКРЫВАЕТСЯ с игроком (смотри визуализацию ниже)
        const obstacle = { x: 120, y: 220, width: 100, height: 100 };

        /*
         * ВИЗУАЛИЗАЦИЯ:
         *
         *     100              180
         *      ┌───────────────┐
         *  200 │   Player      │
         *      │       ┌───────┼────────┐
         *      │       │ OVERLAP│        │
         *  280 └───────┼───────┘        │
         *          120 │   Obstacle     │
         *              └────────────────┘
         *                              220
         *
         * Они перекрываются → должна быть коллизия!
         */

        // 2. ACT (Действие) + ASSERT (Проверка)
        // expect() = "ожидаю что..."
        // .toBe(true) = "...результат будет true"
        expect(MathUtils.checkAABB(player, obstacle)).toBe(true);

        // Если результат НЕ true → тест провален ❌
        // Если результат true → тест пройден ✅
    });

    /**
     * ТЕСТ 2: Проверка что коллизия НЕ обнаруживается когда объекты далеко
     */
    it('НЕ должен обнаружить столкновение когда игрок далеко от препятствия', () => {
        // Игрок слева
        const player = { x: 100, y: 200, width: 80, height: 80 };

        // Препятствие ДАЛЕКО справа (x: 300)
        const obstacle = { x: 300, y: 200, width: 100, height: 100 };

        /*
         * ВИЗУАЛИЗАЦИЯ:
         *
         *  100      180   <- пустое пространство ->   300      400
         *   ┌────────┐                                  ┌────────┐
         *   │ Player │         НЕТ OVERLAP              │Obstacle│
         *   └────────┘                                  └────────┘
         *
         * Они НЕ перекрываются → коллизии быть НЕ должно
         */

        // Ожидаем FALSE (нет коллизии)
        expect(MathUtils.checkAABB(player, obstacle)).toBe(false);
    });

    /**
     * ТЕСТ 3: Проверка масштабирования хитбоксов
     *
     * В игре хитбоксы часто меньше спрайтов (0.7x для игрока).
     * Это делает игру более fair - касания краями не считаются.
     */
    it('должен корректно учитывать масштаб хитбокса', () => {
        // Хитбокс игрока: 80 * 0.7 = 56 пикселей
        const playerHitbox = { x: 100, y: 100, width: 56, height: 56 };

        // Монета маленькая и ВНУТРИ хитбокса игрока
        const coin = { x: 110, y: 110, width: 30, height: 30 };

        /*
         * ВИЗУАЛИЗАЦИЯ:
         *
         *  100                156
         *   ┌──────────────────┐
         *   │  Player Hitbox   │
         *   │    ┌──────┐      │
         *   │    │ Coin │      │
         *   │    └──────┘      │
         *   └──────────────────┘
         *
         * Монета внутри → коллизия должна быть
         */

        expect(MathUtils.checkAABB(playerHitbox, coin)).toBe(true);
    });

    /**
     * ТЕСТ 4: Граничный случай (Edge Case)
     *
     * Что если коробки касаются КРАЯМИ, но не перекрываются?
     * В AABB collision это считается НЕ коллизией (строго <, а не <=)
     */
    it('НЕ должен обнаружить коллизию когда коробки касаются только краями', () => {
        // Первая коробка: от x=0 до x=10
        const boxA = { x: 0, y: 0, width: 10, height: 10 };

        // Вторая коробка: начинается ровно в x=10
        const boxB = { x: 10, y: 0, width: 10, height: 10 };

        /*
         * ВИЗУАЛИЗАЦИЯ:
         *
         *  0     10   10    20
         *  ┌─────┐┌─────┐
         *  │BoxA ││BoxB │   <- Касаются краем в x=10
         *  └─────┘└─────┘
         *
         * Касание края без overlap → НЕТ коллизии
         * (В реальной игре это правильно, иначе будет несправедливо)
         */

        expect(MathUtils.checkAABB(boxA, boxB)).toBe(false);
    });
});